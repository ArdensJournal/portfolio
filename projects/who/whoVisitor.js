document.getElementById('search-regular').addEventListener('click', function() {
    const firstName = document.getElementById('visitor-first-name').value.toLowerCase();
    const lastName = document.getElementById('visitor-last-name').value.toLowerCase();
    const company = document.getElementById('visitor-company').value.toLowerCase();
    const id = document.getElementById('visitor-id').value;

    const filteredWorkers = workers.filter(worker => {
        return (
            (firstName === "" || worker.FirstName.toLowerCase().includes(firstName)) &&
            (lastName === "" || worker.LastName.toLowerCase().includes(lastName)) &&
            (company === "" || worker.CompanyName.toLowerCase().includes(company)) &&
            (id === "" || worker.ID.includes(id))
        );
    });

    displayFilteredWorkers(filteredWorkers);
});

function displayFilteredWorkers(filteredWorkers) {
    const filteredWorkersDiv = document.getElementById('filtered-workers');
    filteredWorkersDiv.innerHTML = '';

    if (filteredWorkers.length === 0) {
        filteredWorkersDiv.innerHTML = '<p>לא נמצאו תוצאות</p>';
        return;
    }

    const ul = document.createElement('ul');
    filteredWorkers.forEach(worker => {
        const li = document.createElement('li');
        li.textContent = `${worker.FirstName} ${worker.LastName}, ${worker.CompanyName}, ID: ${worker.ID}`;
        
        const addButton = document.createElement('button');
        addButton.textContent = 'הוסף לרשימת מבקרים';
        addButton.addEventListener('click', function() {
            addVisitor(worker);
        });

        li.appendChild(addButton);
        ul.appendChild(li);
    });

    filteredWorkersDiv.appendChild(ul);
}

function addVisitor(worker) {
    const visitorsList = JSON.parse(localStorage.getItem('visitorsList')) || [];
    
    const currentTime = new Date().toLocaleTimeString('he-IL');
    const visitor = {
        timeOfEntrance: currentTime,
        name: `${worker.FirstName} ${worker.LastName}`,
        id: worker.ID,
        vehicle: false
    };

    visitorsList.push(visitor);
    localStorage.setItem('visitorsList', JSON.stringify(visitorsList));

    alert('מבקר נוסף בהצלחה!');
}
